package com.ob_ex.introprogramming;

public class Person {
    private int age;
    private String name;
    private int tel;

    public Person(int age, String name, int tel) {
        this.age = age;
        this.name = name;
        this.tel = tel;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }

    public int getTel() {
        return tel;
    }
}


